$(document).ready(function() {
    $('#popupForm').submit(function(e) {
        e.preventDefault();

        var name = $('#name2').val();
        var email = $('#email2').val();
        var phone = $('#phone2').val();
        var message = $('#message2').val();

        var error = false;

        var name_regex = /^[a-zA-Z\s]*$/;
        var email_regex =
            /\b(^(\S+@).+((\.com)|(\.net)|(\.edu)|(\.mil)|(\.gov)|(\.org)|(\.info)|(\.in)|(\.biz)|(\..{2,2}))$)\b/gi;
        var mobile_regex = /\d{10}/;

        if (!name.match(name_regex) || name === "") {
            $('#check2').html('** Please Enter your Name **').css("color", "red");
            $("#name2").focus();
            return false;
        }
        if (!email.match(email_regex) || email === "") {
            $('#check2').html('** Please Enter a Valid Email **').css("color", "red");
            $("#email2").focus();
            return false;
        }
        if (!phone.match(mobile_regex) || phone === "") {
            $('#check2').html('** Please Enter a Valid Phone No **').css("color", "red");
            $("#phone2").focus();
            return false;
        }

        if (message === "") {
            $('#check2').html('** Please Enter Your Message **').css("color", "red");
            $("#message2").focus();
            return false;
        }

        if (!error) {
            $('#popup_submit_btn').attr('disabled', true).val('Sending...');
            var form = $('#popupForm')[0];
            var formData = new FormData(form);
            $.ajax({
                url: "popup_valid.php",
                type: "POST",
                data: formData,
                contentType: false,
                processData: false,
                success: function(data) {
                    if (data == 'sent') {
                        $('#popup_submit_btn').removeAttr('disabled').val('Sent');
                        $('#check2').html(
                            '** Thank You For Contacting Us, We Will Contact You Soon. **'
                            ).css("color", "green");
                        $("#popupForm")[0].reset();
                    } else {
                        $('#popup_submit_btn').removeAttr('disabled').val('Retry');
                        $('#check2').html(data).css("color", "red");
                    }
                }
            });
        }
    });
});
 </script>


